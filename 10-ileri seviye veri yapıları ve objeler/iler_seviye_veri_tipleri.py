# bin() fonksiyonu, sayıyı 2lik tabanda yazmaya yarar.  (0 ve 1)
print(bin(4))
print(bin(19))


# hex() fonksiyonu, sayıyı 16lık tabana çevirmeyi sağlar
print(hex(32))
print(hex(54))


# abs() fonksiyonu, sayının mutlak değerini almamızı sağlar.
print(abs(-4))
print(abs(-3456))
print(abs(4564))


# round() fonksiyonu, sayıları alta veya üste yuvarlar
print(round(4.8))
print(round(4.5))
print(round(4.4))
print(round(3.2229, 2))


# max() fonksiyonu içine veren değerlerin en büyüğünü döndürür.
# min() fonksiyonu içine verilen değerlerin en küçüğünü döndürür.

print(max([1,2,3,4,5,6,7]))
print(min([12,3,4,5,6,67,7,]))
print(max(1,2,3,-1))
print(min(1,2,3,-1))


#sum() fonksiyonu, verilen değerleri toplar. Değerlerin liste, demet vb. şeklinde verilmesi gerekir.

print(sum([1,2,3,4,5,6.7]))
print(sum((-1,0,1)))


#pow() fonksiyonu verilen sayının üstünü almayı sağlar. soldaki sayı taban, sağdaki sayı üstür.

print(pow(2,4))


